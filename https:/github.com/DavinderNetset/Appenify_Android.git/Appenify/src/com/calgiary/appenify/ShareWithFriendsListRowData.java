package com.calgiary.appenify;

class ShareWithFriendsListRowData {

	int imgId;
	String name;

	 ShareWithFriendsListRowData(int imgId, String name) {
		super();
		this.imgId = imgId;
		this.name = name;
	}

	/**
	 * @return the imgId
	 */
	public int getImgId() {
		return imgId;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	 
	 
	
}
